            '2. **Variables:**',
            '   - Define catalog variables (text, reference, select box, checkbox, etc.).',
            '   - Set mandatory flags and validation (regex, reference qualifiers).',
            '   - Organize with variable sets if there are reusable field groups.',
            '',
            '3. **Approval Rules:**',
