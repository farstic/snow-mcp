            '   - Define choice lists for state/category fields.',
            '2. **Define relationships:**',
            '   - One-to-many: use reference fields.',
            '   - Many-to-many: use M2M tables (m2m_* naming convention).',
            '   - Parent-child: use the parent field pattern.',
