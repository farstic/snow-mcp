import type { CapabilityDefinition } from '../types.js';

const capability: CapabilityDefinition = {
  name: 'build-atf-suite',
  title: 'Generate ATF Test Suite',
  description:
    'AI-generates runnable ATF test suites from business rules, script includes, flows, or any ServiceNow artifact',
  category: 'build',
  arguments: [
    {
      name: 'target',
      description: 'Business rule, script include, flow name/sys_id, or feature description to test',
      required: true,
    },
    {
      name: 'coverage',
      description: 'Coverage level: basic (happy path), standard (happy + edge cases), comprehensive (all paths + negative). Default: standard',
      required: false,
    },
  ],
  recommendedTools: [
    'snow_scr_business_rule_read',
    'snow_scr_script_include_read',
    'snow_scr_client_script_read',
    'snow_flow_flow_read',
    'snow_atf_atf_suites_index',
    'snow_atf_atf_tests_index',
    'snow_atf_atf_suite_exec',
    'snow_atf_atf_test_exec',
    'snow_atf_atf_failure_insight_read',
    'snow_core_table_schema_read',
    'snow_core_records_query',
    'snow_core_record_add',
  ],
  buildPrompt(args = {}) {
    const target = args.target ?? '<not provided>';
    const coverage = args.coverage ?? 'standard';

    return [
      {
        role: 'user' as const,
        content: {
          type: 'text' as const,
          text: [
            `Generate a **${coverage}** ATF test suite for: **${target}**`,
            '',
            'Follow these steps precisely:',
            '',
            '### Step 1 — Identify and Fetch the Artifact',
            `Determine what "${target}" refers to and retrieve its full definition:`,
            '- **get_business_rule** — if it\'s a business rule name or sys_id',
            '- **get_script_include** — if it\'s a script include',
            '- **get_client_script** — if it\'s a client script',
            '- **query_records** on `sys_hub_flow` — if it\'s a flow',
            '- **get_table_schema** — for any tables involved',
            '',
            'Read the full source code, conditions, and configuration.',
            '',
            '### Step 2 — Analyze Testable Behaviors',
            `Coverage level: **${coverage}**`,
            '',
            '**Basic:** Test the primary happy path only.',
            '**Standard:** Happy path + boundary conditions + key error paths.',
            '**Comprehensive:** All execution paths including:',
            '- Normal/expected inputs',
            '- Boundary values (empty strings, max length, null references)',
            '- Invalid/unexpected inputs',
            '- Role-based access (test with different user roles)',
            '- Concurrent/timing scenarios where applicable',
            '',
            'For each testable behavior, identify:',
            '1. Preconditions (data setup needed)',
            '2. Action (what triggers the behavior)',
            '3. Expected outcome (what to assert)',
            '',
            '### Step 3 — Create the ATF Test Suite',
            'Use **create_record** on table `sys_atf_test_suite` to create the parent suite:',
            '```',
            '{',
            '  table: "sys_atf_test_suite",',
            '  fields: {',
            '    name: "ATF: <artifact_name> - <coverage> coverage",',
            '    description: "<auto-generated description>",',
            '    active: true',
            '  }',
            '}',
            '```',
            '',
            '### Step 4 — Create Individual Test Cases',
            'For each test scenario, create a test record on `sys_atf_test`:',
            '```',
            '{',
            '  table: "sys_atf_test",',
            '  fields: {',
            '    name: "Test: <scenario_name>",',
            '    description: "<what this test validates>",',
            '    active: true',
            '  }',
            '}',
            '```',
            '',
            '### Step 5 — Create Test Steps',
            'For each test case, create steps on `sys_atf_step`. Common step types:',
            '',
            '**Setup steps:**',
            '- `sn_atf_step_impersonate` — Impersonate a user with specific role',
            '- `sn_atf_step_set_field_values` — Set field values on a record',
            '- `sn_atf_step_run_server_side_script` — Run setup script',
            '',
            '**Action steps:**',
            '- `sn_atf_step_create_record` — Create a record (triggers business rules)',
            '- `sn_atf_step_update_record` — Update a record',
            '- `sn_atf_step_open_a_form` — Open a form (for client script testing)',
            '- `sn_atf_step_run_server_side_script` — Execute custom logic',
            '',
            '**Assertion steps:**',
            '- `sn_atf_step_assert_record_values` — Assert specific field values',
            '- `sn_atf_step_assert_record_exists` — Assert a record was created',
            '- `sn_atf_step_assert_record_not_found` — Assert no matching record',
            '- `sn_atf_step_run_server_side_script` — Custom assertions via gs.assert()',
            '',
            '**Cleanup steps:**',
            '- `sn_atf_step_delete_record` — Clean up test data',
            '',
            '### Step 6 — Link Tests to Suite',
            'Create `sys_atf_test_suite_test` records linking each test to the suite.',
            '',
            '### Step 7 — Run and Validate',
            'Use **run_atf_suite** to execute the suite. If any tests fail:',
            '1. Use **get_atf_failure_insight** to analyze failures',
            '2. Fix the test steps based on the insight',
            '3. Re-run until all tests pass',
            '',
            '### Output Format',
            'After creating all artifacts, provide:',
            '- Suite sys_id and name',
            '- List of test cases with their purpose',
            '- Total test count by coverage area',
            '- Any tests that were skipped with reasoning',
          ].join('\n'),
        },
      },
    ];
  },
};

export default capability;
